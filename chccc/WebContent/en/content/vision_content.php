<p>Mission Statement﹕</p>
<p>
With the primary burden and focus being on the local Chinese community, Cherry Hill Chinese Christian Church strives for 1) leading non-believers to believe in Jesus Christ to be born again and turn to the true God, and to become members in God’s family; 2) equipping the believers to become spiritually mature Christians, to serve in the church, to love one another, to witness the Gospel with their lives, so to glorify God’s name.
