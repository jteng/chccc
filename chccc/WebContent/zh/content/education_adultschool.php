<p>
<li><u>以賽亞書</u>（高瑛，101室 ）</li>
<br>以賽亞書與申命記關系密切，又是新約引用最多的一部書。先知預言將要來到的彌賽亞，神的仆人，會潔淨祂的百姓並帶來真正的榮耀和祝福。神的百姓由於悖逆會面臨被擄，但是神應許將會帶領他們歸回。公義與罪惡，審判與復興，降卑與升高，患難與祝福，神的救恩，智慧，榮耀，祂對百姓的應許，安慰，鼓勵，警告 - 66章的內容常常令我們望而卻步。我們會一起先學“地圖”- 對全書有概括的了解，以及背景（王下15-20，代下26-32），然后了解以賽亞書和摩西五經以及新約的聯系，並且在具體查考中體會作者語言的優美，所強調的主題，以及今天的應用。
</p>

