<li><u>专题 - 神合用的器皿</u>（于慕潔牧師 - 星期六早上，2016）</li>
<br>知道在神的家中當怎樣行。一、真認識祂並明白祂的旨意、作有見識的管家；二、明白聖經全面的教會真理、作神忠心的管家；三、清楚明白並培養各人恩賜、作百般恩賜管家；四、如何在教會同心合意事奉、作主聰明的工頭；五、教會如何遵行主的大使命、遍傳天國的福音；六、如何關懷扶持挽回眾聖徒、愛神愛人活出愛；七、如何培育新人及年輕同工、傳承天國的福音。
</p>

