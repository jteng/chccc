<p>
<li><u>慕道班</u>（恽涛，  江怀德，图书馆）</li>
<br>本课程的目的是向尚未信主以及愿意寻求的朋友们介绍上帝和耶稣基督的救恩。课程的内容包括：上帝的存在，圣经的可靠性，人的罪，耶稣基督和祂的救恩，得救的途径等主题。希望通过对各个主题的介绍，和朋友们彼此的讨论分享，大家可以一起来探索生命的意义。但愿这个课程能帮助您认识那位独一的真神，得着祂对您人生的祝福。
</p>

<br>
<p>
<li><u>浸礼预备/基要真理班</u>（周功耀，朱晨，303室）</li>
<br>本课程的授课对象是已经决志并愿意受洗的朋友。课程的目标是通过系统的学习﹐使学员对自己的得救有确切的把握﹐对自己所信的有清楚的认识﹐从而使学员得以信心稳固﹐愿藉圣礼来表白信仰﹐且愿意成为教会的会员并承担责任。在本课程中我们将回答﹕什么是救恩？谁是基督耶稣？为什麽要受洗？如何读经及祷告？等等问题。本课程注重基要真理方面的教导，也欢迎所有在这方面尚有疑问的弟兄姊妹参加。
</p>

<br>
<p>
<li><u>四福音书 </u>（丘湘怡, 101室）</li>
<br>因著人的堕落到一个地步，在新约与旧约之间有四百年之久，神没有兴起任何先知代他说话，亦称为“静默期”。然而耶和华的宝座安定在天，他统管万有，也掌管历史，两约之间的演变亦是神的准许。而神为了我们的罪，为了公义与慈爱的平衡，神赐下独生子，为我们的罪受死被钉十字架。这四福音书主要记载了主耶稣的生平。我们可以从君王，仆人，人子及神子不同方面来更认识主耶稣，以及主耶稣在世时的教导。四福音就是整部圣经的重点所在，是旧约预言的历史中心，是新约神学的事实根据。四福音是新约与旧约两条总线的接口，也是旧约里所有支线会合的中心，就是历史中的拿撒勒人耶稣。由以犹太人为重的这边“转变”到以基督徒为重的那边；由神与人立旧的约“转变”为立新的约；由以摩西为主转变为以基督为主；由律法时代“转变”为恩典时代。本课程注重基督生平的学习，欢迎所有在这方面有追求的弟兄姊妹参加。
</p>

<br>
<p>
<li><u>旧约概论（上）</u>（高瑛 ，婴儿室）</li>
<br>旧约占有整本圣经四分之三的篇幅，是由约三十位作者，从摩西开始到玛拉基结束，其中经过了约一千年的时间才写作完成。其中的内容非常丰富，包括了神的创造，人的堕落，神的公义，神的拣选与神的救赎。读旧约可帮助我们认识新约的背景，并更明白耶稣基督的救恩。然而许多信徒都发现读旧约很难。本课程将逐卷分析旧约各书卷的信息与意义，期望能帮助上课的信徒对旧约能有初步的了解，并能自己读旧约圣经。
</p>

<br>
<p>
<li><u>加拉太书 </u>（冯衡生， 胡闽虎， Commons 121室）</li>
<br>加拉太书是一卷流畅有力的护教著作，为新约圣经重要的真理－称义成圣－作辨护；人称义因信耶稣基督；人成圣也不是靠努力去遵行律法，乃是藉着顺服，而这顺服是因相信神的工作，就是神藉着基督和圣灵的恩典与能力，在人里面所成就的工作。加拉太书的要旨是帮助基督徒明白他们的身份决定了他们当在主里过怎样的生活。书中说“我们若是靠圣灵得生，就当靠圣灵行事。”(加5：25）这部书从福音的本质入手，阐明了有新生命的人们当有的特质。通过保罗自己的见证和神的应许在人类历史中的实现，告诫我们“受割礼不受割礼都无关紧要，要紧的就是作新造的人。”(加6：15）希望通过学习加拉太书，我们可以更加明白十字架的意义，并且让圣灵能够在我们的生命中结出更丰硕的果子来。
</p>

