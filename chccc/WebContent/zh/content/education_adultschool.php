<p>
<li><u>羅馬書</u>（黃媽媽，Commons 121室）</li>
<br>羅馬書是保羅寫給羅馬教會的書信, 它是新約書信中最有糸統地研究基督教教義的一本。在羅馬書中，保羅將聖經中的偉大主題都歸納起來，並將這些主題有系統地連系起來－這些主題包括因信稱義, 罪、律法、審判、福音，成聖、揀選、聖靈的工作、基督徒的盼望、神的計劃中猶太人和外邦人的地位、教會的功能、個人生活中敬虔與德行的原則，等等。 這是一部值得反復學習、查考和應用的寶書。 
</p>


