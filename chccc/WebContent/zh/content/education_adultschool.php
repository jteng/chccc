<p>
<li><u>救恩論</u>（李牧师，主堂）</li>
<br>本課的目的是講解聖靈在基督徒身上施行基督完成的救贖工作。屬靈經驗固然是重要的，但它必須建立在穩固的救恩真理根基之上。本課的主旨是將教義的知識和屬靈的經驗結合起來。將探討的課題包括＂恩贖計劃＂、＂恩召＂、＂重生＂、＂信心＂、＂悔改＂、＂揀選＂、＂與基督聯合＂等等。所使用的主要教材是《磐石之上》（作者是李牧師的神學教授）。學生可選擇在上課時購買此書。 
</p>

