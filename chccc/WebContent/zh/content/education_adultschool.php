<p>
<li><u>聖徒裝備與靈命塑造</u>（于慕潔牧師，胡闽虎，101室）</li>
<br>基督徒的成長是雙軌前進的，真理的認識和靈命的塑造，前者重在裝備信徒信仰堅定、並且得以成為教導別人的人；後者重在生命的更新和變化、在生活、事奉和家庭中活出基督。在真理認識上：從聖經神學角度明白純正的聖經教導，注重在基要真理、新約神學、宣教學、䕶教學和認識三一真神。在靈命塑造上：由新舊聖經教訓及人物，探討生命更新成熟的關鍵和榜樣、成為今時代神所喜悅並合用的貴重器皿。本課程將依學員生命成長及學習程度而增設相應內容（如釋經學、教會歷史和系統神學等）。
</p>

