<li><u>慕道班</u>（恽涛，303室）</li>
<br>本課程的目的是曏尚未信主以及願意尋求的朋友們介紹上帝和耶穌基督的救恩。課程的內容包括：上帝的存在，聖經的可靠性，人的罪，耶穌基督和祂的救恩，得救的途徑等主題。希望通過對各個主題的介紹，和朋友們彼此的討論分享，大家可以一起來探索生命的意義。但願這個課程能幫助您認識那位獨一的真神，得著祂對您人生的祝福。
