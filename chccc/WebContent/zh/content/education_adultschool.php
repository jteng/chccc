<p>
<li><u>摩西五经 </u>（傅頡，涂漢明， Commons 121室）</li>
<br>人們通常稱聖經的頭五卷書，即《創世紀》、《出埃及記》、《利未記》、《民數記》和《申命記》為摩西五經，猶太人稱摩西五經為“妥拉”（即“律法”或“訓示”）。摩西五經記錄神如何向人類揭示自己，神從他的創造和揀選中，顯示出他絕對的主權，救贖的能力，他的聖潔，他的恩慈，和他的信實。祂揀選了以色列人只因神愛他們，要他們成為一個祭司、先知和宣教的民族。整卷舊約，甚至整本聖經，都是以這五卷書為基礎。因為一切揭示的真理和神的救贖計劃的基礎，都是建立在摩西五經之上。本次課將以《創世紀》為主，通過幾個重要聖經人物將這五卷書作概括講述。
</p>

