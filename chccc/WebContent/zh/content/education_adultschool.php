<li><u>专题 - 家庭，职业，服事 </u>（于張瑜芬師母，薛斌，101室）</li>
<br>从圣经人物观察学习，他们生命的成长挣扎，軟弱，遭遇.  因着顺服，神的手如何改变了他们的一生.   我们又如何应用我们所领会的，表明在我们的生活里，顕明在不同的场合里（家庭/职业/服事），活出耶稣的生命，荣耀主名.
</p>

