<p>
<li><u>詩篇選讀簡介</u>（黃媽媽，丘湘婷， Commons 121室）</li>
<br>詩篇在全本聖經中，具有獨特地位：（1）全本聖經都是 神藉著萬事，萬物，向人啟示他自己，只有“詩篇”是人在生活中，透過各種經歷，看見神的作為和心意，而向 神發出內心真摯的感情，見証 神的恩典而發出 神聖的讚美和感謝。（2）詩篇是聖經中，作者最多的一卷書，而每卷都有獨立的主題信息。（3）詩篇是新約引用最多的書，引用舊約共287次，其中有116次引自詩篇。（4） 詩篇是聖經中為人最愛讀的一卷書，有些新約聖經都附詩篇，就可知，因為它不是一卷說教的書，它是古聖敬畏神的智者的生活經歷，及見証，使我們讀時有同感，會共鳴，因人類的基本難處，與對環境的反應是不受時空，人種，年齡，性別，區分的。（5） 詩篇是古時的詩篇： 耶穌，保羅，西拉，歷代的殉道者，在赴死前都會唱。（6）馬丁路德, 受（詩篇46）之激勵，寫岀了“神是我們堅固保障”，被稱為是宗教改革進行曲。（7）詩篇被編在聖經的中心，這似乎告訴我們神重視人對他的回應。詩篇119:89 耶和華阿！你的話安定在天，直到永遠。是聖經最中心的一節。
</p>

