<li><u>同工培訓</u>（滕靖，于慕潔牧師，101室）</li>
<br>本課程旨在通過培訓使得小組同工們可以更好地在小組里服事。 本課程內容包括：角色定位、栽培門徒、帶領查經，關懷牧養、靈命塑造、領導團隊、聚會設計和小組敬拜。 本課程注重寓學于練，提供多次課內練習的機會，通過練習和討論學員們可以實踐在課程里所學的方法和技巧，互相借鑑取長補短。歡迎各個團契小組推薦同工參加。
</p>

